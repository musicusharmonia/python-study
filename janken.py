"""
じゃんけんゲーム（ステップ1: 1回勝負）
まずは最もシンプルな形で、基本の仕組みを理解する
"""

import random  # ランダムにコンピュータの手を選ぶために使う

# --- 1. コンピュータの手を決める ---
# リスト: 複数の値をまとめて入れる箱のようなもの
hands = ["グー", "チョキ", "パー"]

# random.choice() はリストの中からランダムに1つ選ぶ関数
computer = random.choice(hands)

# --- 2. プレイヤーの手を入力してもらう ---
print("じゃんけんゲーム！")
print("0: グー、1: チョキ、2: パー")

# 正しい入力が来るまでループで繰り返す
# while True: 条件なしで無限ループ → break で抜け出すまで繰り返す
while True:
    # input() はユーザーの入力を「文字列」として受け取る
    text = input("あなたの手を選んでください（0/1/2）: ")

    # int() に変換する前に文字列のままチェックする
    # なぜ先にチェック？ → "abc" を int() に渡すとエラーでクラッシュするため
    if text in ["0", "1", "2"]:  # 0, 1, 2 のいずれかなら OK
        choice = int(text)       # 文字列 → 数値に変換
        break                    # ループを抜けて次の処理へ進む
    else:
        print("無効な入力です。0, 1, 2 のいずれかを入力してください。")

# リストは番号（インデックス）で中身を取り出せる
# hands[0] → "グー"、hands[1] → "チョキ"、hands[2] → "パー"
player = hands[choice]

# --- 3. 結果を表示する ---
print(f"あなた: {player}  vs  コンピュータ: {computer}")

# --- 4. 勝敗を判定する ---
# まず「引き分け」を判定（同じ手なら引き分け）
if player == computer:
    print("引き分け！")

# 次に「勝ち」のパターンを列挙する
# グー → チョキに勝つ / チョキ → パーに勝つ / パー → グーに勝つ
elif (player == "グー" and computer == "チョキ") or \
     (player == "チョキ" and computer == "パー") or \
     (player == "パー" and computer == "グー"):
    print("あなたの勝ち！")

# 引き分けでも勝ちでもなければ → 負け
else:
    print("あなたの負け...")
